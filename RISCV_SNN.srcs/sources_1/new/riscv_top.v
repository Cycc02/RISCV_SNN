module riscv_top(
    // PS7 FIXED_IO — required for Zynq PL startup
    inout [53:0] FIXED_IO_mio,
    inout        FIXED_IO_ps_clk,
    inout        FIXED_IO_ps_porb,
    inout        FIXED_IO_ps_srstb,

    output [4:0] gpr_data_o,

    // UART serial I/O for FPGA (115200 8-N-1)
    output        uart_tx_o,
    input         uart_rx_i
    );
    // Simulation-only probes — internal wires, not top-level ports.
    // coremark_tb accesses these via hierarchical path (dut.uart_valid_o etc.)
    wire        uart_valid_o;
    wire [7:0]  uart_char_o;

    // ILA debug probes
    (* mark_debug = "true", KEEP = "TRUE" *) wire        dbg_uart_tx_valid;
    (* mark_debug = "true", KEEP = "TRUE" *) wire        dbg_uart_tx_pre;
    (* mark_debug = "true", KEEP = "TRUE" *) wire        dbg_rstn;
    (* mark_debug = "true", KEEP = "TRUE" *) wire [14:0] dbg_pc;
    (* mark_debug = "true", KEEP = "TRUE" *) wire        dbg_uart_addr;
    (* mark_debug = "true", KEEP = "TRUE" *) wire [7:0]  dbg_uart_char;
    
    // SNN accelerator — driven by CPU CSRs 0xBC0..0xBC5.
    //   exec.csr writes  : snn_img_base (0xBC0), snn_kick (0xBC2)
    //   exec.csr reads   : snn_output_spike (0xBC3), snn_hidden_spike (0xBC4/5)
    //   snn_top reads DTCM via memory_stage port B (snn_dtcm_*)
    wire [11:0] snn_img_base;
    wire        snn_kick;

    wire        snn_dtcm_rd_en;
    wire [11:0] snn_dtcm_addr;
    wire [31:0] snn_dtcm_data;

